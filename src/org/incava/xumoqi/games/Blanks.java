package org.incava.xumoqi.games;

public class Blanks {
	private final Integer total;
	private final Integer index;

	public Blanks(Integer total, Integer index) {
		this.total = total;
		this.index = index;
	}
	
	public Integer getTotal() {
		return total;
	}
	
	public Integer getIndex() {
		return index;
	}
}
